class CalcTest {
public:
	vector <string> uniform(vector <string>);
};

vector <string> CalcTest::uniform(vector <string> numbers) {
	vector<string> ans;
	for(string num : numbers){
		string s = "";
		for(char c : num){
			if(c == ' '){
			
			} else if(c <= '9' && c >= '0'){
				s += c;
			} else {
				s += '.';
			}
		}
		ans.push_back(s);
	}
	return ans;
}


