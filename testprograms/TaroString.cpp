class TaroString {
public:
	string getAnswer(string);
};

string TaroString::getAnswer(string S) {
	int n = SZ(S);
	bool sw = true;
	string x = "CAT";
	int p = 0;
	for(char c : S){
		if(c == x[p]){
			p++;
			if(p == 3){
				break;
			}
		}
	}
	sw &= (p == 3);
	int c = 0, a = 0, t = 0;
	for(char z : S){
		c += (z == 'C');
		a += (z == 'A');
		t += (z == 'T');
	}
	sw &= (c == 1) && (a == 1) && (t == 1);
	return sw? "Possible" : "Impossible";
}


