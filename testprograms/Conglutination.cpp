class Conglutination {
public:
	string split(string, int);
};

string Conglutination::split(string conglutination, int expectation) {
	long long a, b, e = expectation;
	string ans;
	int n = conglutination.size();
	bool sw = false;
	for(int k = 1 ; k < n ; k++){
		a = 0; b = 0; ans = "";
		for(int i = 0 ; i < n ; i++){
			if(i == k){
				ans += "+";
			}
			ans += conglutination[i];
			if(i < k){
				a *= 10LL;
				a += conglutination[i]-'0';
			} else {
				b *= 10LL;
				b += conglutination[i]-'0';
			}
			
		}
		if(a + b == e){
			sw = true;
			break;
		}
	}
	return sw ? ans : "";
}


