class LongWordsDiv2 {
public:
	string find(string);
};
string yes = "Likes" , no = "Dislikes";
string LongWordsDiv2::find(string word) {
	int n = word.size();
	bool sw = true;
	for(int i = 0 ; i < n - 1 ; i++){
		if(word[i] == word[i+1]){
			return no;
		}
	}
	for(int i = 0 ; i < n ; i++){
		for(int j = i+1 ; j < n ; j++){
			for(int k = j+1 ; k < n ; k++){
				for(int q = k+1 ; q < n ; q++){
					if(word[i] == word[k] && word[j] == word[q]){
						return no;
					}
				 } 
			}	
		} 
	}
	return yes;
}


